#importing the Numpy library
import numpy as np
#Initializing the arr
zeros_arr = np.zeros(10)
ones_arr = np.ones(10)
fives_arr = np.full(10, 5)
print("Array of zeros:", zeros_arr)
print("Array of ones:", ones_arr)
print("Array of fives:", fives_arr)


