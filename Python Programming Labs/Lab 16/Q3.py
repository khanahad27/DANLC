#3.Find the maximum and minimum values in an array.
#importing the Numpy library
import numpy as np
#Initializing the arr
arr = np.arange(1,11)
#Using Max & Min function to fetch maximum and minimum values from the array
max_value = arr.max()
min_value = arr.min()
print("Maximum value:", max_value)
print("Minimum value:", min_value)

